# Calculates a dogs age in human years by simple convesrsion || Python Version

# The age of the dog
dogAge = 2

# First 2 dog years is 21 years in human time
earlyYears = 21

# After 2 years every year is 4 years in human time
laterYears = (dogAge-2)*4

# Total years
humanYears = earlyYears+laterYears

print(f"My name is Ruffles! Ruff ruff, I am {humanYears} old in human years.\n")